#!/usr/bin/env node
/**
 * Copies original exam MP3s from provas/ → public/provas/ (same filenames)
 * and generates data/exams/audioManifest.ts for track → file mapping.
 *
 * Run: npm run setup:audio
 */
import { copyFileSync, existsSync, mkdirSync, readdirSync, writeFileSync } from "fs";
import { join } from "path";

const ROOT = new URL("..", import.meta.url).pathname;
const PROVAS = join(ROOT, "provas");
const PUBLIC_PROVAS = join(ROOT, "public", "provas");
const MANIFEST_OUT = join(ROOT, "data", "exams", "audioManifest.ts");

const VERSIONS = [
  { version: "23C", folder: "Prova 23C" },
  { version: "24C", folder: "Prova 24C" },
  { version: "25C", folder: "Prova 25C" },
  { version: "26C", folder: "Prova 26C" },
];

function sortAudioFiles(files) {
  return [...files].sort((a, b) => {
    const parse = (name) => {
      const track = name.match(/^Track(\d+)\.mp3$/i);
      if (track) return parseInt(track[1], 10);

      const m = name.match(/(\d{2})-(\d{2})-(\d{2})(?: (\d+))?\.mp3$/i);
      if (!m) return 0;
      const base = parseInt(m[1], 10) * 3600 + parseInt(m[2], 10) * 60 + parseInt(m[3], 10);
      const suffix = m[4] ? parseInt(m[4], 10) : 0;
      return base * 10 + suffix;
    };
    return parse(a) - parse(b);
  });
}

const manifest = {};
const folders = {};

for (const { version, folder } of VERSIONS) {
  const srcDir = join(PROVAS, folder);
  folders[version] = folder;

  if (!existsSync(srcDir)) {
    console.warn(`Skip ${version}: ${srcDir} not found`);
    manifest[version] = {};
    continue;
  }

  const mp3s = readdirSync(srcDir).filter((f) => f.toLowerCase().endsWith(".mp3"));
  const sorted = sortAudioFiles(mp3s);
  const destDir = join(PUBLIC_PROVAS, folder);
  mkdirSync(destDir, { recursive: true });

  manifest[version] = {};
  sorted.forEach((file, i) => {
    const track = i + 1;
    manifest[version][track] = file;
    copyFileSync(join(srcDir, file), join(destDir, file));
    console.log(`${version} TRACK ${track} ← ${file}`);
  });
}

const ts = `/** Auto-generated by scripts/setup-exam-audio.mjs — do not edit manually */
import type { ExamVersion } from "@/lib/exams/types";

export const EXAM_AUDIO_FOLDERS: Record<ExamVersion, string> = ${JSON.stringify(folders, null, 2)} as const;

/** Track number (1–14) → original filename in provas folder */
export const EXAM_AUDIO_FILES: Record<ExamVersion, Record<number, string>> = ${JSON.stringify(manifest, null, 2)} as const;

export const SOUND_CHECK_TRACK = 1;
`;

writeFileSync(MANIFEST_OUT, ts);
console.log(`\nWrote ${MANIFEST_OUT}`);
console.log("Done — originals served from /provas/");
